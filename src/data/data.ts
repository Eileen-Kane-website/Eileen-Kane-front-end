export const bio = "Eileen S. Kane was born in Cambridge, MA and educated at Smith College (AB,'67-Zoology), Harvard University (MS,'69 and Ph.D.,'67-Anatomy) and the Pacific Northwest College of Art (BFA equivalent, '87-painting & drawing). She has shown her work nationally and internationally in many one-person and group shows. Kane published her large, charcoal drawings and related poems in: Forms of Protest: Drawings and Poems about Racism (Joseph Press, 1994). She has taught a variety of art classes at the grade school, college and graduate levels as well as within community art centers. Kane was an Art Instructor (drawing, painting) at colleges in Portland (OR) & Walters Cultural Arts Center (Hillsboro,OR). She taught anatomy and art through Saturday Academy (Portland, OR). Her work can be seen (Portland, OR) at the Rental/Sales Gallery of the Portland Art Museum, and with the National Association of Women Artists (NY, NY). Kane has work in numerous private and public collections throughout the Northwest and the United States, including the Corvallis (OR) City Hall and Portland Community College. Kane has been a speaker and panelist for several public forums as well as a juror for art exhibits in the Northwest. She is married and has two married adult children & 3 grandchildren.";


